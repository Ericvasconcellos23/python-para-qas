# dados do tipo inteiro int
status_code = 200
quantidade_usuarios = 15
tentativas = 5

print(status_code)
print(quantidade_usuarios)
print(tentativas)

# dados do tipo flutuante float
tempo_resposta = 1.42
valor_total = 199.90

print(tempo_resposta)
print(valor_total)

# dados do tipo texto str
nome = 'Luana'
sobrenome = 'Barbiere'

print(nome)
print(sobrenome)

# dados do tipo boolean bool
login_valido = True
usuario_ativo = False

print(login_valido)
print(usuario_ativo)

# dados do tipo nulo None
token = None
print(token)

# Descobrindo o tipo da variável

